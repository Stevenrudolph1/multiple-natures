## Can I use MN for...?

### ✅ YES (Permitted)
- Personal orientation and self-exploration
- Professional coaching/consulting (with training)
- Academic research and citation
- Career exploration support

### ⚠️ REQUIRES LICENSE
- Commercial/organizational integration
- MN-branded tools or programs
- Derivative assessments
