## Purpose

Establishes how others may create MN-inspired tools, training programs, and derivative works.

## Core Principle

**Ideas are open. Implementations are owned. Endorsement is non-transferable.**

## What Is Open (No License Required)

- MN theory and concepts (10 MIs, 9 MNs, swastha, feed your tigers, etc.)
- Personal application
- Citation in research/academic work

## What Requires License

- The MNTEST (professional/commercial use)
- MN-branded tools and programs
- Derivative works at scale

## What Is Prohibited

- Use that violates MN boundaries (identity assignment, prediction, prescription)
- Distortion of MN theory
- Misrepresentation of endorsement

## Licensing Categories

1. **Research/Academic** - Generally granted freely with attribution
2. **Professional Practice** - Training-based
3. **Organizational/Commercial** - Formal agreement required
4. **Derivative Tool Creation** - Collaborative agreement

